public class pattern_1 {
   public static void main(String[] args) {

    int n=5;
    int m=5;

    for (int i=1; i<=n; i++) {
        for (int j=1; j<=m; j++) {
            System.out.print("x ");
        }
        System.out.println();
      }
   }
}
